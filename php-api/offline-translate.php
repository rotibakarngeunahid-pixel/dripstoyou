function offline_words_en_id(): array {
    $words = [];
    foreach (offline_words_id_en() as $id => $en) {
        $words[mb_strtolower($en, 'UTF-8')] = $id;
    }
    return $words;
}

function offline_cap_like(string $source, string $target): string {
    if ($source === '' || $target === '') return $target;
    if (preg_match('/^[A-Z]/', $source)) {
        return mb_strtoupper(mb_substr($target, 0, 1, 'UTF-8'), 'UTF-8') . mb_substr($target, 1, null, 'UTF-8');
    }
    return $target;
}

function offline_replace_phrases(string $text, array $pairs): string {
    usort($pairs, fn($a, $b) => strlen($b[0]) <=> strlen($a[0]));
    foreach ($pairs as $pair) {
        [$from, $to] = $pair;
        $pattern = '~(^|[^\p{L}\p{N}])(' . preg_quote($from, '~') . ')(?=$|[^\p{L}\p{N}])~iu';
        $text = preg_replace_callback($pattern, function ($m) use ($to) {
            return $m[1] . offline_cap_like($m[2], $to);
        }, $text);
    }
    return $text;
}

function offline_replace_words(string $text, array $words): string {
    return preg_replace_callback('~\p{L}[\p{L}\'-]*~u', function ($m) use ($words) {
        $key = mb_strtolower($m[0], 'UTF-8');
        return isset($words[$key]) ? offline_cap_like($m[0], $words[$key]) : $m[0];
    }, $text);
}

function offline_detect_language(string $text): string {
    $lower = mb_strtolower($text, 'UTF-8');
    preg_match_all('~\p{L}+~u', $lower, $matches);
    $tokens = $matches[0] ?? [];
    $idMarkers = ['yang', 'dan', 'atau', 'dengan', 'untuk', 'tidak', 'kami', 'anda', 'di', 'ke', 'dari', 'dalam', 'bisa', 'akan', 'jadwal', 'lokasi', 'layanan', 'area', 'treatment', 'kesehatan', 'pemulihan'];
    $enMarkers = ['the', 'and', 'or', 'with', 'for', 'not', 'we', 'you', 'your', 'our', 'in', 'to', 'from', 'is', 'are', 'can', 'will', 'schedule', 'location', 'service', 'area', 'treatment', 'health', 'recovery'];
    $idScore = 0;
    $enScore = 0;
    foreach ($tokens as $token) {
        if (in_array($token, $idMarkers, true)) $idScore++;
        if (in_array($token, $enMarkers, true)) $enScore++;
    }
    foreach (offline_phrase_pairs_id_en() as [$phrase]) {
        if (str_contains($lower, $phrase)) $idScore += str_contains($phrase, ' ') ? 2 : 1;
    }
    foreach (offline_phrase_pairs_en_id() as [$phrase]) {
        if (str_contains($lower, $phrase)) $enScore += str_contains($phrase, ' ') ? 2 : 1;
    }
    return $idScore >= $enScore ? 'id' : 'en';
}

function offline_cleanup_en(string $text): string {
    $pairs = [
        '~\bteam we\b~i' => 'our team',
        '~\bteam our\b~i' => 'our team',
        '~\bcertified medical team come\b~i' => 'certified medical team comes',
        '~\bmedical team come\b~i' => 'medical team comes',
        '~\bour team come\b~i' => 'our team comes',
        '~\byou needs\b~i' => 'your needs',
        '~\byou location\b~i' => 'your location',
        '~\bservice active\b~i' => 'active service',
        '~\barea service\b~i' => 'service area',
        '~\bmedical team certified\b~i' => 'certified medical team',
        '~\s+([,.!?;:])~' => '$1',
        '~[ \t]{2,}~' => ' ',
    ];
    return trim(preg_replace(array_keys($pairs), array_values($pairs), $text));
}

function offline_cleanup_id(string $text): string {
    return trim(preg_replace(['~\s+([,.!?;:])~', '~[ \t]{2,}~'], ['$1', ' '], $text));
}

function offline_translate_text(string $text, string $targetLang, string $sourceLang = 'auto'): string {
    if (trim($text) === '') return $text;
    $source = in_array($sourceLang, ['en', 'id'], true) ? $sourceLang : offline_detect_language($text);
    if ($source === $targetLang) return $text;

    $lines = preg_split('~(\r?\n)~', $text, -1, PREG_SPLIT_DELIM_CAPTURE);
    $result = [];
    foreach ($lines as $line) {
        if (preg_match('~^\r?\n$~', $line) || trim($line) === '') {
            $result[] = $line;
            continue;
        }
        if ($source === 'id' && $targetLang === 'en') {
            $result[] = offline_cleanup_en(offline_replace_words(offline_replace_phrases($line, offline_phrase_pairs_id_en()), offline_words_id_en()));
        } elseif ($source === 'en' && $targetLang === 'id') {
            $result[] = offline_cleanup_id(offline_replace_words(offline_replace_phrases($line, offline_phrase_pairs_en_id()), offline_words_en_id()));
        } else {
            $result[] = $line;
        }
    }
    return implode('', $result);
}
